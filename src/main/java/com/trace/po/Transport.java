package com.trace.po;

public class Transport {

	private String transport_id;
	private String transport_truck; //运输车
	private String transport_user; //运输人
	private String transport_destination; //目的地
	private String coordinate; //坐标
	private String transport_pic; //运输照片
	
	
	public String getTransport_id() {
		return transport_id;
	}
	public void setTransport_id(String transport_id) {
		this.transport_id = transport_id;
	}
	public String getTransport_truck() {
		return transport_truck;
	}
	public void setTransport_truck(String transport_truck) {
		this.transport_truck = transport_truck;
	}
	public String getTransport_user() {
		return transport_user;
	}
	public void setTransport_user(String transport_user) {
		this.transport_user = transport_user;
	}
	public String getTransport_destination() {
		return transport_destination;
	}
	public void setTransport_destination(String transport_destination) {
		this.transport_destination = transport_destination;
	}
	public String getCoordinate() {
		return coordinate;
	}
	public void setCoordinate(String coordinate) {
		this.coordinate = coordinate;
	}
	public String getTransport_pic() {
		return transport_pic;
	}
	public void setTransport_pic(String transport_pic) {
		this.transport_pic = transport_pic;
	}
	
	
}
